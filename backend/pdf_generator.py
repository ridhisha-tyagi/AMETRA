def build_chart_table(chart_data):

    table_data = [["Planet", "Sign", "House", "Degree", "Retrograde"]]

    for planet, data in chart_data["D1"].items():

        table_data.append([
            planet,
            data["sign"],
            data.get("house", "-"),
            f"{round(data['degree'], 2)}°",
            "Yes" if data["retrograde"] else "No"
        ])

    table = Table(
        table_data,
        colWidths=[90, 90, 80, 90, 100]
    )

    table.setStyle(TableStyle([

        ("BACKGROUND", (0, 0), (-1, 0), GOLD),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.black),

        ("TEXTCOLOR", (0, 1), (-1, -1), LIGHT_TEXT),

        ("ALIGN", (0, 0), (-1, -1), "CENTER"),

        ("FONTNAME", (0, 0), (-1, 0), "TitleFont"),
        ("FONTNAME", (0, 1), (-1, -1), "BodyFont"),

        ("GRID", (0, 0), (-1, -1), 0.5, GOLD)

    ]))

    return table

def extract_tarot(archetype):

    tarot = archetype.get("tarot")

    if isinstance(tarot, dict):
        return tarot.get("neutral", "Unknown")

    return tarot or "Unknown"
    
def build_intensity_table(pii_scores):

    systems = {
        "Sun": "Identity System",
        "Moon": "Emotional System",
        "Mercury": "Thinking & Communication System",
        "Venus": "Relationship Pattern",
        "Mars": "Action System",
        "Jupiter": "Expansion & Wisdom System",
        "Saturn": "Pressure & Karmic System",
        "Rahu": "Growth Drive",
        "Ketu": "Detachment Pattern"
    }

    def get_label(score):

        if score >= 6:
            return "Dominant"

        elif score >= 4:
            return "Strong"

        else:
            return "Present"

    table_data = [["Psychological System", "Intensity"]]

    for planet, system in systems.items():

        score = pii_scores.get(planet, 3)

        label = get_label(score)

        table_data.append([system, label])

    table = Table(
        table_data,
        colWidths=[260, 120]
    )

    table.setStyle(TableStyle([

        ("BACKGROUND", (0,0), (-1,0), GOLD),
        ("TEXTCOLOR", (0,0), (-1,0), colors.black),

        ("TEXTCOLOR", (0,1), (-1,-1), LIGHT_TEXT),

        ("ALIGN", (0,0), (-1,-1), "CENTER"),

        ("FONTNAME", (0,0), (-1,0), "TitleFont"),
        ("FONTNAME", (0,1), (-1,-1), "BodyFont"),

        ("GRID", (0,0), (-1,-1), 0.5, GOLD)

    ]))

    return table    
    
def build_profile_summary(archetypes, pii_scores):
    
    core = archetypes.get("core", {})
    growth = archetypes.get("growth", {})

    core_title = core.get("title", "Unknown")
    growth_title = growth.get("title", "Unknown")

    qualities = core.get("qualities", [])
    limitations = core.get("limitations", [])

    qualities_text = ", ".join(qualities[:4])
    limitations_text = ", ".join(limitations[:3])
    
    # determine dominant / strong systems from pii scores
    dominant_systems = []
    strong_systems = []

    for planet, score in pii_scores.items():

        system = SYSTEM_NAMES.get(planet, planet)

        if score >= 6:
            dominant_systems.append(system)

        elif score >= 4:
            strong_systems.append(system)
            
    dominant_text = "<br/>".join(dominant_systems)
    strong_text = "<br/>".join(strong_systems)        

    core = archetypes.get("core", {})
    growth = archetypes.get("growth", {})

    core_name = core.get("title", "Unknown")
    growth_name = growth.get("title", "Unknown")

    systems = {
        "Sun": "Identity System",
        "Moon": "Emotional System",
        "Mercury": "Thinking & Communication System",
        "Venus": "Relationship Pattern",
        "Mars": "Action System",
        "Jupiter": "Expansion & Wisdom System",
        "Saturn": "Pressure & Karmic System",
        "Rahu": "Growth Drive",
        "Ketu": "Detachment Pattern"
    }

    dominant = []
    strong = []

    for planet, score in pii_scores.items():

        name = systems.get(planet, planet)

        if score >= 6:
            dominant.append(name)

        elif score >= 4:
            strong.append(name)

    dominant_text = ", ".join(dominant) if dominant else "None"
    strong_text = ", ".join(strong) if strong else "None"

    qualities = core.get("qualities", [])
    limitations = core.get("limitations", [])

    q_text = ", ".join(qualities[:4])
    l_text = ", ".join(limitations[:3])
    
    table_data = [

    ["Core Archetype", Paragraph(core_title, body_style)],

    ["Growth Direction", Paragraph(growth_title, body_style)],

    ["Dominant Systems", Paragraph(dominant_text, body_style)],

    ["Strong Systems", Paragraph(strong_text, body_style)],

    ["Key Qualities", Paragraph(qualities_text, body_style)],

    ["Development Focus", Paragraph(limitations_text, body_style)]

    ]
        
    table = Table(table_data, colWidths=[200, 300])

    table.setStyle(TableStyle([

        ("BACKGROUND",(0,0),(0,-1),GOLD),
        ("TEXTCOLOR",(0,0),(0,-1),colors.black),

        ("TEXTCOLOR",(1,0),(1,-1),LIGHT_TEXT),

        ("FONTNAME",(0,0),(-1,-1),"BodyFont"),

        ("GRID",(0,0),(-1,-1),0.5,GOLD)

    ]))

    return table    
    
