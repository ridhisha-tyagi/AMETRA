def page_decor(canvas, doc):

    width, height = LETTER

    # background
    canvas.setFillColor(BLACK)
    canvas.rect(0, 0, width, height, fill=1, stroke=0)

    # gold border
    canvas.setStrokeColor(GOLD)
    canvas.setLineWidth(1)
    canvas.rect(30, 30, width - 60, height - 60)

    # watermark logo for pages after cover
    if doc.page > 1:

        logo_path = os.path.join(BASE_DIR, "assets", "ametra_logo.png")

        if os.path.exists(logo_path):

            canvas.saveState()

            canvas.setFillAlpha(0.05)

            canvas.drawImage(
                logo_path,
                width / 2 - 170,
                height / 2 - 170,
                width=340,
                height=340,
                mask="auto"
            )

            canvas.restoreState()

    canvas.setFont("BodyFont", 9)
    canvas.setFillColor(LIGHT_TEXT)

    canvas.drawCentredString(width / 2, 35, "Generated by Ametra")


